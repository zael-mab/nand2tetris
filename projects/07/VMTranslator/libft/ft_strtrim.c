#include "libft.h"

char	*ft_strtrim(char const *s)
{
	int i;
	int j;

	i = 0;
	if (s)
	{
		j = ft_strlen(s) - 1;
		while (s[i] && (s[i] == ' ' || s[i] == '\n' || s[i] == '\t'))
			i++;
		while (j > i && (s[j] == ' ' || s[j] == '\n' || s[j] == '\t'))
			j--;
		return (ft_strsub(s, i, j - i + 1));
	}
	return (NULL);
}
