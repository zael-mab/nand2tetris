#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		j0;
	int		j1;
	char	*n;

	j0 = 0;
	j1 = 0;
	n = (char *)s;
	while (n[j0])
		j0++;
	while (j0 >= 0)
	{
		if (n[j0] == c)
			return (&n[j0++]);
		j0--;
	}
	return (0);
}
