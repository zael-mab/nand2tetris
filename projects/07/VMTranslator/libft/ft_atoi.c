#include "libft.h"

int		ft_atoi(const char *str)
{
	int a;
	int b;
	int c;

	a = 0;
	b = 0;
	c = 0;
	while ((str[a] >= 9 && str[a] <= 13) || str[a] == 32)
		a++;
	if (str[a] == '-')
		c = 1;
	if (str[a] == '+' || str[a] == '-')
		a++;
	while (str[a] >= '0' && str[a] <= '9' && str[a] != '\0')
	{
		b = b * 10 + (str[a] - 48);
		a++;
	}
	if (c == 1)
		return (-b);
	else
		return (b);
}
