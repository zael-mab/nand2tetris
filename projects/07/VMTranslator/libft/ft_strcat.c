#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t j;
	size_t i;

	j = 0;
	i = 0;
	while (s1[j])
		j++;
	while (s2[i] != '\0')
	{
		s1[j] = s2[i];
		j++;
		i++;
	}
	s1[j] = '\0';
	return (s1);
}
