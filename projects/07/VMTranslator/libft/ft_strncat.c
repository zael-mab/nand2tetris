#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t j;
	size_t i;

	j = 0;
	i = 0;
	while (s1[j])
		j++;
	while (s2[i] != '\0' && i < n)
	{
		s1[j] = s2[i];
		j++;
		i++;
	}
	s1[j] = '\0';
	return (s1);
}
